let cardData = []
function addToCart(){
    let itemToAdd = this.id;
    console.log("this obj",this);
    console.log('id',itemToAdd);
    let itemObj = foodItem.find(item=>item.id==itemToAdd);
    let itemIndexInCart = cardData.indexOf(itemObj);
    if(itemIndexInCart === -1){
        cardData = [...cardData,itemObj];
        document.getElementById(itemToAdd).classList.add('toggle-heart');
    }else{
        alert("added to card already");
    }
    document.getElementById('cart-plus').innerText = ' ' +
    cardData.length + ' Items';
    totalAmount();
    cartItems();
}

function cartItems(){
    let tableBody = document.getElementById('table-body');
    tableBody.innerHTML = '';

    cardData.map(item=>{
        let tableRow = document.createElement('tr');
        let imageData = document.createElement('td');
        let img = document.createElement('img');
        img.src = item.img;
        imageData.appendChild(img);

        let itemNameData = document.createElement('td');
        itemNameData.innerText = item.name;

        let quantityData = prepareQuantityData(item);

        let priceData = document.createElement('td');
        priceData.innerText = item.price;

        tableRow.appendChild(imageData);
        tableRow.appendChild(itemNameData);
        tableRow.appendChild(quantityData);
        tableRow.appendChild(priceData);

        tableBody.appendChild(tableRow);
    })

    document.querySelectorAll('.increase-item').forEach(item=>item.addEventListener('click',incrementItem));
    document.querySelectorAll('.decrease-item').forEach(item=>item.addEventListener('click',decrementItem));

    function prepareQuantityData(item) {
        let quantityData = document.createElement('td');
        let decrementButton = document.createElement('button');
        decrementButton.setAttribute('class', 'decrease-item');
        decrementButton.innerText = '-';
        let quantityInfo = document.createElement('span');
        quantityInfo.innerText = item.quantity;
        let incrementButton = document.createElement('button');
        incrementButton.setAttribute('class', 'increase-item');
        incrementButton.innerText = '+';

        quantityData.appendChild(decrementButton);
        quantityData.appendChild(quantityInfo);
        quantityData.appendChild(incrementButton);
        return quantityData;
    }
}

function incrementItem(){
    console.log("Item to be incremented",this);
    console.log("Item to be decremented",this);
    let itemToIncrement = this.parentNode.previousSibling.innerText;
    console.log("item name to be decremented",itemToIncrement);
    let incrementObj = cardData.find(item=>item.name==itemToIncrement);
    incrementObj.quantity+=1;
    cartItems();
    totalAmount();
}

function decrementItem(){
    console.log("Item to be decremented",this);
    let itemToDecrment = this.parentNode.previousSibling.innerText;
    console.log("item name to be decremented",itemToDecrment);
    let decrementObj = cardData.find(item=>item.name==itemToDecrment);
    if(decrementObj.quantity>1){
        decrementObj.quantity-=1;
    }else{
        let index = cardData.indexOf(decrementObj);
        document.getElementById(decrementObj.id).classList.remove('toggle-heart');
        cardData.splice(index,1);
        document.getElementById('cart-plus').innerText = ' ' + cardData.length + ' Items';
        if(cardData.length<1){
            document.getElementById('food-items').classList.toggle('food-items');
            document.getElementById('category-list').classList.toggle('food-items');
            document.getElementById('category-header').classList.toggle('toggle-category');
            document.getElementById('cart-page').classList.toggle('cart-toggle');
            document.getElementById('checkout').classList.toggle('cart-toggle');
        }
    }
    cartItems();
    totalAmount();
}

function totalAmount(){
    let totalQuantity = cardData.reduce((acc,item)=>{
        return acc+item.quantity;
    },0);
    let totalPrice = cardData.reduce((acc,item)=>{
        return acc+(item.quantity*item.price);
    },0);
    document.getElementById('total-item').innerText = 'Total Items: ' + totalQuantity;
    document.getElementById('total-price').innerText = 'Total Price: ' + totalPrice;
}


function cartToggle(){
    if(cardData.length>0){
        document.getElementById('food-items').classList.toggle('food-items');
        document.getElementById('category-list').classList.toggle('food-items');
        document.getElementById('category-header').classList.toggle('toggle-category');
        document.getElementById('cart-page').classList.toggle('cart-toggle');
        document.getElementById('checkout').classList.toggle('cart-toggle');
    } else{
        alert("Currently there are no items in the cart");
    }
}

function addEvents(){
    document.querySelectorAll('.add-to-cart')
    .forEach(item=>item.addEventListener('click',addToCart));

    document.getElementById('cart-plus').addEventListener('click',cartToggle);

    document.querySelectorAll('.cart-btn').forEach(item=>{
        item.addEventListener('click',()=>{
            cardData=[];
            alert("Order placed successfully");
            window.location.reload();
        })
    })

    document.getElementById('add-address').addEventListener('click',addAddress);

}

function addAddress(){
    let address  = prompt("Enter your address");
    if(address){
        document.getElementById('add-address').innerText=` ${address}`;
    }else{
        alert("Invalid address");
    }
}

