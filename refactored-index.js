function displayItems(){
    let categories = ['biryani','chicken','paneer','vegetable','chinese','south-indian'];
    categories.forEach((categoryName)=>{
        let categoryElement = document.getElementById(categoryName);
        const itemsData = foodItem.filter((item)=>item.category===categoryName);
        itemsData.forEach((item)=>{
            let itemCard = prepareItemCard(item);
            categoryElement.appendChild(itemCard);
        })
    })
}

displayItems();

function prepareItemCard(item){
    //Item card to be made
    console.log(item);
    let itemCard = document.createElement("div");
    itemCard.setAttribute('id','item-card');

    let cardTop = document.createElement('div');
    cardTop.setAttribute('id','card-top');

    let star = document.createElement('i');
    star.setAttribute('class','fa fa-star rating');
    star.setAttribute('id','rating');
    star.innerText = ""+ item.rating;

    let heart = document.createElement('i');
    heart.setAttribute('class','fa fa-heart-o add-to-cart');
    heart.setAttribute('id',item.id);

    cardTop.appendChild(star);
    cardTop.appendChild(heart);

    let img = document.createElement('img');
    img.src = item.img;

    let itemName = document.createElement('p');
    itemName.setAttribute('id','item-name');
    itemName.innerText = item.name;

    let itemPrice = document.createElement('p');
    itemPrice.setAttribute('id','item-price');
    itemPrice.innerText = `Price : $ ${item.price}`;

    itemCard.appendChild(cardTop);
    itemCard.appendChild(img);
    itemCard.appendChild(itemName);
    itemCard.appendChild(itemPrice);

    return itemCard;
}

let cartData = [];

function addToCart(e){
    console.log(e);
    console.log(this.id, typeof(this.id));
    const foodObj = foodItem.find(item=>item.id==this.id);
    console.log(foodObj);
    if(foodObj==null){
        alert("Item not available");
    }else{
        //not added in the cart
            // add item in the cart
            //update class for heart to indicate its been added in cart
        //Added in the cart
            //Alert added to the cart
        const foodObjCartIndex = cartData.indexOf(foodObj);
        if(foodObjCartIndex===-1){
            foodObj.quantity = 1;
            cartData.push(foodObj);
            // cartData = [...cartData,foodObj];
            document.getElementById('cart-plus').innerText = ' ' + cartData.length + " Items";
            document.getElementById('m-cart-plus').innerText = ' ' + cartData.length + " Items";
            document.getElementById(this.id).classList.add('toggle-heart');
            totalAmount();
            cartItems();
        }else{
            alert("Already added in cart");
        }
    }   
}



function totalAmount(){
    let totalAmount  = 0;
    let totalQuantity = 0;
    cartData.forEach((item)=>{
        totalQuantity+=item.quantity;
        totalAmount+=item.price*item.quantity;
    });
    document.getElementById('total-item').innerText = `Total Items: ${totalQuantity}`;
    document.getElementById('total-price').innerText = `Total Price: ${totalAmount}`;
}

function cartItems(){
    let tableBody = document.getElementById('table-body');
    tableBody.innerHTML = "";
    cartData.forEach((item)=>{
        let tableRow = document.createElement('tr');

        let imageCell = document.createElement('td');
        let img = document.createElement('img');
        img.src = item.img;
        imageCell.appendChild(img);

        let itemName = document.createElement('td');
        itemName.innerHTML = item.name;


        let itemQuantity = document.createElement('td');
        let decrementButton = document.createElement('button');
        decrementButton.setAttribute('class','decrease-item');
        decrementButton.setAttribute('id',item.id);
        decrementButton.innerText="-";
        let quantity = document.createElement('span');
        quantity.innerText= item.quantity;
        let incrementButton = document.createElement('button');
        incrementButton.setAttribute('class','increase-item');
        incrementButton.setAttribute('id',item.id);
        incrementButton.innerText="+";
        itemQuantity.appendChild(decrementButton);
        itemQuantity.appendChild(quantity);
        itemQuantity.appendChild(incrementButton);

        let itemPrice = document.createElement('td');
        itemPrice.innerHTML = item.price;

        tableRow.appendChild(imageCell);
        tableRow.appendChild(itemName);
        tableRow.appendChild(itemQuantity);
        tableRow.appendChild(itemPrice);

        tableBody.appendChild(tableRow);
    });

    document.querySelectorAll('.increase-item').forEach((item)=>{
        item.addEventListener('click',incrementItem);
    });
    document.querySelectorAll('.decrease-item').forEach((item)=>{
        item.addEventListener('click',decrementItem);
    });
}

function incrementItem(){
   let itemObj = cartData.find((item)=>item.id==this.id);
   itemObj.quantity+=1;
   totalAmount();
   cartItems();
}

function decrementItem(){
    let itemObj = cartData.find((item)=>item.id==this.id);
    itemObj.quantity-=1;
    if(itemObj.quantity<1){
        //remove that item from the table
        cartData.splice(cartData.indexOf(itemObj),1);
        document.getElementById(itemObj.id).classList.remove('toggle-heart');
        document.getElementById('cart-plus').innerText = ' ' + cartData.length + " Items";
        document.getElementById('m-cart-plus').innerText = ' ' + cartData.length + " Items";
        //no items in the cart
        if(cartData.length<1){
            alert("No items in the cart");
            hidecart();
        }
    }
    totalAmount();
    cartItems();
}

function cartToggle(){
    if(cartData.length>0){
        hidecart();
    }else{
        alert("Nothing in the cart");
    }
}

function hidecart(){
    document.getElementById('food-items').classList.toggle('food-items');
    document.getElementById('cart-page').classList.toggle('cart-toggle');
    document.getElementById('category-list').classList.toggle('food-items');
    document.getElementById('checkout').classList.toggle('cart-toggle');

    document.getElementById('m-cart-plus').classList.toggle('m-cart-toggle')
    document.getElementById('category-header').classList.toggle('toggle-category');
}

function addAddress(){
    let address = prompt("Enter your address");
    if(address){
        document.getElementById('add-address').innerText = ` ${address}`;
    }else{
        alert("Invalid address");
    }
}


function addEvents(){
    document.querySelectorAll('.add-to-cart').forEach((item)=>{
        item.addEventListener('click',addToCart);
    });
    document.getElementById('cart-plus').addEventListener('click',cartToggle);

    document.getElementById('add-address').addEventListener('click',addAddress);

    document.getElementsByClassName('cart-btn')[0].addEventListener('click',completeOrder);
}
addEvents();

function completeOrder(){
    cartData = [];
    alert("Order placed successfully");
    document.querySelectorAll('.toggle-heart').forEach((item)=>{
        item.classList.remove('toggle-heart');
    });
    document.getElementById('cart-plus').innerText = ' ' + cartData.length + " Items";
    document.getElementById('m-cart-plus').innerText = ' ' + cartData.length + " Items";
    hidecart();
}

